# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (true) ou não (false).
# Existem vãrios operadores para "questionar".
# Dentre eles 0 ==, que é um operador lógivo que 
# questiona se um valor é igual a outro

print(10 == 10)
print(10 == 11)
print(type(True))
print(type(False))
print(type (10 == 10))
print(type(10 == 11))
